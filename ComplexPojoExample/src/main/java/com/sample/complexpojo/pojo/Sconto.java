public class Sconto {

	private double percSconto;
	private String tipoSconto;
	public double getPercSconto() {
		return percSconto;
	}
	public void setPercSconto(double percSconto) {
		this.percSconto = percSconto;
	}
	public String getTipoSconto() {
		return tipoSconto;
	}
	public void setTipoSconto(String tipoSconto) {
		this.tipoSconto = tipoSconto;
	}

	
}
