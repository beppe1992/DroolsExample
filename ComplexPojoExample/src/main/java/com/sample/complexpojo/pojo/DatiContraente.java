package com.sample.complexpojo.pojo;

public class DatiContraente {

	private int etaContraente = 0;
	private String tipoCliente;
	private String nominativo;

	public String getTipoCliente() {
		return tipoCliente;
	}

	public void setTipoCliente(String tipoCliente) {
		this.tipoCliente = tipoCliente;
	}

	public int getEtaContraente() {
		return etaContraente;
	}

	public void setEtaContraente(int etaContraente) {
		this.etaContraente = etaContraente;
	}

	public String getNominativo() {
		return nominativo;
	}

	public void setNominativo(String nominativo) {
		this.nominativo = nominativo;
	}

}
